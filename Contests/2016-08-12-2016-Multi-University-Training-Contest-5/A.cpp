double dfs(int warn, int delta){
	
	if(warn < 0) return inf;
	if(f[warn][delta] > -eps) return f[warn][delta];
	f[warn][delta] = inf;
	if(delta == 0) return f[warn][delta] = 0;
	
	for(int i = 1; i <= delta; ++i){
		double p = 1.00 * (delta - i + 1) / (delta + 1);
		double q = 1.00 - p;
		double pexp = dfs(warn, delta - i);
		double qexp = dfs(warn - 1, i - 1);
		f[warn][delta] = min(f[warn][delta], p * pexp + q * qexp + 1.00);
	}
	return f[warn][delta];
}

